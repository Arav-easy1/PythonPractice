# 증감연산자: 기존에 사용하던 증가/감소 기능을 짧게 이용
# 축약형 = > 증감연산자
# 관계 연산자: 두개의 값을 비교하여 관계
# A == B : A와 B가 같은지 판별 => True, False
# A != B : A와 B가 다른지 판별 => True, False
# A > B : A가 B보다 큰지 판별
# A < B : A가 B보다 작은지 판별
# 논리 연산자: 여러 개의 수식을 논리적으로 연산
# A and B: A와 B가 모두 참인지 판별
# A or B: A 혹은 B가 참인지 판별
# not A: A가 거짓인지 판별
if 3 > 5 or 7 < 10:
    print("야호!")